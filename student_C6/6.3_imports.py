# we can import a module and use the functions, and variables (and objects)
# the '.' sytax means use the object inside the module

import math

print('round down 5.3 = ', math.floor(5.3))

print('cosine of pi', math.cos(math.pi)) # math.pi is a variable

print('natural log of e', math.log(math.e)) # math.e is a variable
